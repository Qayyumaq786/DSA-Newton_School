package basics;

public class ForLoop {
    public static void main(String[] args) {
        /**
         * Write a program to print numbers between 1 to 100 using a for loop.
         */
        for(int i=1; i <= 100 ; ++i){
            System.out.println(i);
        }
        // i=1 -> 2 -> 3 .. . ........ 100 . Once i becomes 101

        // WAP to print only odd numbers from 1 to 100 using for loop.

    }
}
